#include "libft.h"

void	ft_printab(char **str)
{
	int		i;

	i = 0;
	if (str)
	{
		while (str[i] != NULL)
		{
			ft_printf("%s", str[i]);
			i++;
		}
		ft_printf("\n");
	}
}